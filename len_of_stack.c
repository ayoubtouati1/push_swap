#include "push_swap.h"

int	len_of_stack(t_stk *a)
{
	int		i;
	t_stk	*tmp;

	i = 0;
	tmp = a;
	while (a)
	{
		i++;
		a = a->nxt;
	}
	a = tmp;
	return (i);
}
